package com.syntax.class06and07;

public class PracTask {

	public static void main(String[] args) {
		// print number from 30-70 using do while loop
		// print odd from 99-1 using do while

		
		int n=30;
		do {
			System.out.print(n);
			n++;
		
		} while (n<=70);
		System.out.println("");
		
		
		int x=89;
		do { 
			System.out.println(x+"");
			x--;
		}while (x>=1);
	
	}

}
