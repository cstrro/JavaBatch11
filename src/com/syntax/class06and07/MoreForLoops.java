package com.syntax.class06and07;

public class MoreForLoops {

	public static void main(String[] args) {
		// Whats the output
		
		
		int sum=0;
		
		for(int i=1; i<=6; i++) {
			sum+=1;
			System.out.println(sum);
		}
		
		int result=0;
		
		for(int i=0; i<=4; i+=2) {
			result*=i;
			System.out.println(result);
		}

	}

}
